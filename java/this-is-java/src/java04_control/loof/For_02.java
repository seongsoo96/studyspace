package java04_control.loof;

public class For_02 {
	public static void main(String[] args) {
		
		//반복문에서는 i 변수를 많이 사용한다
		//	iterate의 첫 글자로 사용한 것
		
		for(int i=0; i<10; i++) {
			System.out.println(i); //0-~9
		}
		
		//i변수가 0부터 10보다 작은 동안에 1씩 증가하며 반복코드가 실행된다
		
		//반복횟수: 10번
		//반복구간: 0~9
		
		int i;
		
		for(i=0; i<10; i++) {
			System.out.println(i); //0~9
		}
		System.out.println(i); //10이 출력됨, 조건식이 false가 되도록한 값
		
		
		
		
		
	}
}
